=begin
input: array
output: boolean
rules:
- works like Enumerable#none?
- returns flase if block returns true for any element
- stops searching once block returns true
- returns true for empty array regardless of block

examples:
none?([1, 3, 5, 6]) { |value| value.even? } == false
none?([1, 3, 5, 7]) { |value| value.even? } == true
none?([2, 4, 6, 8]) { |value| value.odd? } == true
none?([1, 3, 5, 7]) { |value| value % 5 == 0 } == false
none?([1, 3, 5, 7]) { |value| true } == false
none?([1, 3, 5, 7]) { |value| false } == true
none?([]) { |value| true } == true

algorithm:
- possibly return true if array is empty
- iterate through array:
  - return false if block returns true for element
- return true
=end

def none?(arr)
  arr.each { |el| return false if yield(el) }
  true
end

