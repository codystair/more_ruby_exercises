=begin
input: array and block
output: depends
rules:
- works like Enumerable#max_by
- returns element that contains largest value returned from block

examples:
max_by([1, 5, 3]) { |value| value + 2 } == 5
max_by([1, 5, 3]) { |value| 9 - value } == 1
max_by([1, 5, 3]) { |value| (96 - value).chr } == 1
max_by([[1, 2], [3, 4, 5], [6]]) { |value| value.size } == [3, 4, 5]
max_by([-7]) { |value| value * 3 } == -7
max_by([]) { |value| value + 5 } == nil

algorithm:
- set values to empty array
- iterate over array:
  - push return of block for element to values
- return max of values
=end

def max_by(arr)
  max = arr.first
  arr.each do |item|
      max = item if yield(item) > yield(max)
  end
  max
end

